namespace SpeakingPractice.Api.Domain.Enums;

/// <summary>
/// IELTS Speaking Part types
/// </summary>
public enum QuestionType
{
    /// <summary>
    /// Part 1: Introduction and Interview (4-5 minutes)
    /// Short questions about personal topics
    /// </summary>
    PART1 = 1,

    /// <summary>
    /// Part 2: Long Turn (3-4 minutes)
    /// Cue card with 1-2 minutes speaking time
    /// </summary>
    PART2 = 2,

    /// <summary>
    /// Part 3: Discussion (4-5 minutes)
    /// Abstract questions related to Part 2 topic
    /// </summary>
    PART3 = 3
}











